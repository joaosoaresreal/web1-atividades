let mes = parseFloat(prompt(`Digite um Número:`))

if (mes == 1){
    document.write(`1 - Janeiro`)
}
else if (mes == 2){
    document.write(`2 - Fevereiro`)
}
else if (mes == 3){
    document.write(`3 - Março`)
}
else if (mes == 4){
    document.write(`4 - Abril`)
}
else if (mes == 5){
    document.write(`5 - Maio`)
}
else if (mes == 6){
    document.write(`6 - Junho`)
}
else if (mes == 7){
    document.write(`7 - Julho`)
}
else if (mes == 8){
    document.write(`8 - Agosto`)
}
else if (mes == 9){
    document.write(`9 - Setembro`)
}
else if (mes == 10){
    document.write(`10 - Outubro`)
}
else if (mes == 11){
    document.write(`11 - Novembro`)
}
else if (mes == 12){
    document.write(`12 - Dezembro`)
}
else {
    document.write(`Número Inválido`)
}